package com.th3rdwave.safeareacontext;

public class Rect {
    public float height;
    public float width;
    public float x;
    public float y;

    public Rect(float f2, float f3, float f4, float f5) {
        this.x = f2;
        this.y = f3;
        this.width = f4;
        this.height = f5;
    }
}
